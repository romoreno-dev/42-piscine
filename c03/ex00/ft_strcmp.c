int	ft_strcmp(char *s1, char *s2)
{
	int	i;
	int	diferencia;

	i = 0;
	while (s1[i] != '\0' || s2[i] != '\0')
	{
		if (s1[i] != s2[i])
		{
			diferencia = s1[i] - s2[i];
			return (diferencia);
		}
		i++;
	}
	return (0);
}
